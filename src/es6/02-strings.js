let hello = 'Hello';
let world = 'world';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase)

// Template literals 
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi line strings
let lorem1 = 'esto es un string \n ' + 'esto es otra linea';
let lorem2 = `Esta es una frase epica en la primera linea 
  esto es la segunda frase en la segunda linea
`;

console.log(lorem1)
console.log(lorem2)